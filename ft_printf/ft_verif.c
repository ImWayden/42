#include "ft_printf.h"

void		ft_verif(char **str , t_list **list)
{
	if (**str && ft_isoption(*str, *list))
	{
		while (!ft_isalpha(**str))
			(*str)++;
		(*str)++;
		*list = (*list)->next;
	}
}
