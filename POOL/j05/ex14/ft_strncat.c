char		*ft_strncat(char *dest, char *src, size_t n)
{
	int		i;

	i = ft_strlen(dest);
	while (*src && n)
	{
		dest[i] = *src++;
		i++;
		n--;
	}
	dest[i] = '\0';
	return (dest);
}
