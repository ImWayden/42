int					ft_sqrt(int nb)
{
	int				i;

	if (nb == 1 || nb == 0)
		return (nb);
	else if (nb > 0)
	{
		i = 0;
		while (42)
		{
			if ((i * i) < nb)
				i++;
			else if ((i * i) > nb)
				return (0);
			else
				return (i);
		}
	}
	else
		return (0);
}
