#include "fractol.h"

void		render(t_env *env)
{
	int		y;
	int		x;

	y = -1;
	while (++y < SCREEN_H)
	{
		x = -1;
		while (++x < SCREEN_W)
			env->fract(env, x, y);
	}
}
