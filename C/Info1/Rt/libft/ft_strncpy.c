#include "libft.h"

char	*ft_strncpy(char *s1, char *s2, int n)
{
	int		i;

	i = 0;
	while (i != n)
	{
		s1[i] = s2[i];
		i = i + 1;
	}
	if (s2[i] != '\0')
		s1[i] = '\0';
	return (s1);
}
