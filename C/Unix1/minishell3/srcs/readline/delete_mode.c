#include "minishell3.h"

int				delete_mode(char **str, int j)
{
	int			i;

	i = ft_strlen(*str);
	if (*str && j > 0 && j <= i)
	{
		i = i - j;
		while ((*str)[i])
		{
			(*str)[i] = (*str)[i + 1];
			i++;
		}
		return (-1);
	}
	return (0);
}
