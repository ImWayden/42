#include "libft.h"

char	*ft_strchr(const char *s, int c)
{
	char *tmp;
	
	tmp = (char *) s;
	while (*tmp != (char) c && s)
	{
		if (*tmp == '\0')
			return (NULL);
		tmp++;
	}
	return (tmp);
}
