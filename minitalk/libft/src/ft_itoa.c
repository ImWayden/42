#include "libft.h"

char		*ft_itoa(int n)
{
	char	s[20];
	int		i;
	int		is_neg;

	if (!n)
		return (ft_strdup("0"));
	is_neg = 1;
	if (n > 0)
	{
		is_neg = 0;
		n = -n;
	}
	i = 0;
	while (n != 0)
	{
		s[i] = -(n % 10) + 48;
		n /= 10;
		i++;
	}
	if (is_neg)
		s[i++] = '-';
	s[i] = '\0';
	return (ft_strdup(ft_strrev(s)));
}
