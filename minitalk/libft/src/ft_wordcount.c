int			ft_wordcount(char const *s, char c)
{
	int		i;
	int		j;

	i = 0;
	j = 0;
	if (!s || !c)
		return (0);
	while (s[i] != '\0')
	{
		if ((s[i + 1] == c || s[i + 1] == '\0'))
			j++;
		i++;
	}
	return (j);
}
