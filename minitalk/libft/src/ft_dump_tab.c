#include <stdlib.h>

void			ft_dump_tab(void ***tab)
{
	int			cur;

	cur = 0;
	while (tab[0][cur])
	{
		free(tab[0][cur]);
		tab[0][cur] = NULL;
		++cur;
	}
	free(tab[0]);
	tab[0] = NULL;
}
