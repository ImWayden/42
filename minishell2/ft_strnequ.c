#include "minishell2.h"

int		ft_strnequ(char *s1, char *s2, size_t n)
{
	int		i;

	i = 0;
	while (s1[i] == s2[i] && n)
	{
		n--;
		if ((s1[i] == s2[i]) && (s1[i] == '\0'))
			return (1);
		i++;
	}
	 if (!n)
	 	return (1);
	return (0);
}
